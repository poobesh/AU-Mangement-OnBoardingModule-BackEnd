package com.accolite.aumanagement.model;

public class Trends {
	
	String company_name;
	int required_employee_count;
	int year;
	
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public int getRequired_employee_count() {
		return required_employee_count;
	}
	public void setRequired_employee_count(int required_employee_count) {
		this.required_employee_count = required_employee_count;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	
	

}
